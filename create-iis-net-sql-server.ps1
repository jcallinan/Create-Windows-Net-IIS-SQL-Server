#SQL Server
function Install-SqlServer {
  [CmdletBinding()]
  param (
    # The path to the SQL Server installation image
    [Parameter(Mandatory = $true)]
    [ValidateNotNullOrEmpty()]
    [String]
    $ImagePath,

    # The extraction location for the installation media, used as the SourcePath for the DSC configuration
    [Parameter(Mandatory = $true)]
    [ValidateNotNullOrEmpty()]
    [String]
    $SourcePath
  )

  begin {
    New-Item -Path $SourcePath -ItemType Directory
    Copy-Item -Path (Join-Path -Path (Get-PSDrive -Name ((Mount-DiskImage -ImagePath $ImagePath -PassThru) | Get-Volume).DriveLetter).Root -ChildPath '*') -Destination $SourcePath -Recurse
    Dismount-DiskImage -ImagePath $ImagePath

    # Ensure NuGet package provider is present
    Get-PackageProvider -Name NuGet -ForceBootstrap

    # Ensure SqlServerDsc module is present
    if (!(Get-Module SqlServerDsc)) {
      Install-Module -Name SqlServerDsc -Force
    }
  }

  process {
    # Compile configuration
    & .\sql-server-configuration.ps1 -SourcePath $SourcePath

    # Run DSC configuration
    Start-DscConfiguration -Path sql-server-configuration -Wait -Force -Verbose
  }

  end {
    if (Test-DscConfiguration) {
      Write-Host 'Removing configuration directory...'
      Remove-Item .\SqlServerConfiguration -Recurse

      Write-Host 'Removing extract directory...'
      Remove-Item $SourcePath -Recurse

      Write-Host 'Done.'
    }
  }
}

